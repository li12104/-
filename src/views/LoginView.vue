<script>
export default {
  data() {
    return {
      loginInfo: {
        username: "",
        pwd: "",
      },
      registerInfo: {
        username: "",
        pwd: "",
        nickname: "",
      },
    };
  },
  methods: {
    begin() {
      const sign_in_btn = document.querySelector("#sign-in-btn");
      const sign_up_btn = document.querySelector("#sign-up-btn");
      const container = document.querySelector(".container");
      sign_up_btn.addEventListener("click", () => {
        container.classList.add("sign-up-mode");
      });
      sign_in_btn.addEventListener("click", () => {
        this.registerInfo.username = "";
        this.registerInfo.pwd = "";
        this.registerInfo.nickname = "";
        container.classList.remove("sign-up-mode");
      });
    },
    checkInfo(n, p) {
      if (n == "" || p == "") {
        this.$mineShowMsg("账号与密码内容不能为空", "warning");
        return false;
      }
      return true;
    },
    login() {
      if (
        this.checkInfo(this.loginInfo.username, this.loginInfo.pwd) == false
      ) {
        return;
      }
      const loading = this.$loading({
        lock: true,
        text: "Loading",
        spinner: "el-icon-loading",
        background: "rgba(0, 0, 0, 0.7)",
      });
      this.$axios({
        url: "/api/user/login",
        method: "post",
        data: this.loginInfo,
      })
        .then((res) => {
          loading.close();
          if (res.data.code == 200) {
            localStorage.setItem("token", res.data.token);
            localStorage.setItem("id", res.data.data.id);
            localStorage.setItem("username", res.data.data.username);
            localStorage.setItem("isAdmin", res.data.data.isAdmin);
            localStorage.setItem("nickname", res.data.data.nickname);
            this.$alert("登录成功，稍后为您跳转页面", "登录状态", {
              confirmButtonText: "确定",
              callback: () => {
                this.$router.replace("/page");
              },
            });
            return;
          }
          this.$mineShowMsg(res.data.msg, "warning");
        })
        .catch(() => {
          loading.close();
          this.$mineShowMsg("系统异常请稍后再试", "error");
        });
    },
    register() {
      if (
        this.checkInfo(this.registerInfo.username, this.registerInfo.pwd) ==
        false
      ) {
        return;
      }
      const loading = this.$loading({
        lock: true,
        text: "Loading",
        spinner: "el-icon-loading",
        background: "rgba(0, 0, 0, 0.7)",
      });
      this.$axios({
        url: "/api/user/register",
        method: "post",
        data: this.registerInfo,
      })
        .then((res) => {
          loading.close();
          if (res.data.code == 200) {
            this.$alert("注册成功，稍后为您跳转登录", "注册状态", {
              confirmButtonText: "确定",
              callback: () => {
                this.loginInfo.username = this.registerInfo.username;
                this.registerInfo.username = "";
                this.registerInfo.pwd = "";
                this.registerInfo.nickname = "";
                const container = document.querySelector(".container");
                container.classList.remove("sign-up-mode");
              },
            });
            return;
          }
          this.$mineShowMsg(res.data.msg, "warning");
        })
        .catch(() => {
          loading.close();
          this.$mineShowMsg("系统异常请稍后再试", "error");
        });
    },
  },
  mounted() {
    this.begin();
  },
};
</script>

